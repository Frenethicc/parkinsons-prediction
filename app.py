import streamlit as st
import pandas as pd
import numpy as np
import pickle

# Load model
with open('parkinsons_model.pkl', 'rb') as f:
    model = pickle.load(f)

# UI Title
st.title("Parkinson's Disease Prediction App")

st.write("Masukkan nilai-nilai berikut untuk memprediksi kemungkinan Parkinson:")

# Input fields
fo = st.number_input('MDVP:Fo(Hz)', value=119.992)
fhi = st.number_input('MDVP:Fhi(Hz)', value=157.302)
flo = st.number_input('MDVP:Flo(Hz)', value=74.997)
jitter_percent = st.number_input('MDVP:Jitter(%)', value=0.00784)
shimmer = st.number_input('MDVP:Shimmer', value=0.04374)
ppe = st.number_input('PPE', value=0.368674)

# Convert to array
input_data = np.array([[fo, fhi, flo, jitter_percent, shimmer, ppe]])

# Prediction
if st.button('Prediksi'):
    prediction = model.predict(input_data)
    if prediction[0] == 1:
        st.error("Orang ini kemungkinan MENGIDAP Parkinson.")
    else:
        st.success("Orang ini kemungkinan TIDAK mengidap Parkinson.")

